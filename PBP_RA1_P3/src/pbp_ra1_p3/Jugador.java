package pbp_ra1_p3;

public class Jugador {
    String nom;
    int nivell;
    int copes;
    int or;
    int gems;
    int estrelles;

    public Jugador(String nom, int nivell, int copes, int or, int gems, int estrelles) {
        this.nom = nom;
        this.nivell = nivell;
        this.copes = copes;
        this.or = or;
        this.gems = gems;
        this.estrelles = estrelles;
    }
}
